let day = '수요일';

switch (day) {
    case "월요일":
        console.log('한 주의 시작! 화이팅!');
        break;
    case "화요일":
        console.log('어제보다 나은 오늘!');
        break;
    case "수요일":
        console.log('벌써 수요일! 반이나 왔어요!');
        break;
    case "목요일":
        console.log('내일이면 금요일!');
        break;
    case "금요일":
        console.log('불금! 주말이 기다려져요!');
        break;
    case "토요일":
        console.log('신나는 토요일!');
        break;
    case "일요일":
        console.log('푹 쉬세요!');
        break;
    default:
        console.log('올바른 요일을 입력해주세요');
}

